package com.imooc.demo1.zhangdemotest.entity;

public class Student {
    private String studentId;
    private String counsellorId;
    private String studentName;
    private String studentSex;
    private String studentDepartment;
    private String studentOpenid;

    public String getStudentId() {
        return studentId;
    }

    public void setStudentId(String studentId) {
        this.studentId = studentId;
    }

    public String getCounsellorId() {
        return counsellorId;
    }

    public void setCounsellorId(String counsellorId) {
        this.counsellorId = counsellorId;
    }

    public String getStudentName() {
        return studentName;
    }

    public void setStudentName(String studentName) {
        this.studentName = studentName;
    }

    public String getStudentSex() {
        return studentSex;
    }

    public void setStudentSex(String studentSex) {
        this.studentSex = studentSex;
    }

    public String getStudentDepartment() {
        return studentDepartment;
    }

    public void setStudentDepartment(String studentDepartment) {
        this.studentDepartment = studentDepartment;
    }

    public String getStudentOpenId() {
        return studentOpenid;
    }

    public void setStudentOpenId(String studentOpenId) {
        this.studentOpenid = studentOpenId;
    }
}
